CREATE TABLE creatures
(
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name         VARCHAR(120)                            NOT NULL,
    species      VARCHAR(120)                            NOT NULL,
    danger_level VARCHAR(30)                             NOT NULL,
    status       VARCHAR(30)                             NOT NULL,
    notes        TEXT,
    created_at   TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    updated_at   TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    habitat_id   BIGINT                                  NOT NULL,
    CONSTRAINT pk_creatures PRIMARY KEY (id)
);

CREATE TABLE habitats
(
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    biome      VARCHAR(40)                             NOT NULL,
    location   VARCHAR(120)                            NOT NULL,
    min_temp_c INTEGER                                 NOT NULL,
    max_temp_c INTEGER                                 NOT NULL,
    created_at TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    CONSTRAINT pk_habitats PRIMARY KEY (id)
);

