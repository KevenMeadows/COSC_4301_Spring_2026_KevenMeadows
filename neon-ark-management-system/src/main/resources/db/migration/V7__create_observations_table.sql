-- Create a way to fill in blank for time observed_at using trigger.
CREATE OR REPLACE FUNCTION set_observed_at_if_null()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.observed_at IS NULL THEN
    NEW.observed_at := CURRENT_TIMESTAMP; -- Applies timestamp from system.
END IF;
RETURN NEW;
END;
$$ LANGUAGE plpgsql;
-- Create the trigger to be used for inserts that have no timestamp for observed_at
CREATE TRIGGER set_observed_at
    BEFORE INSERT ON observations
    FOR EACH ROW
    EXECUTE FUNCTION set_observed_at_if_null();