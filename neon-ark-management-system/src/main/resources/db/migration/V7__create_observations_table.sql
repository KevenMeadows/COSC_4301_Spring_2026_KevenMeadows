-- Create observations table / use previously submitted project 3
CREATE TABLE observations (
                              id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL, -- PK for each observation
                              creature_id BIGINT NOT NULL,
                              observer_name VARCHAR(100) NOT NULL,
                              note_text VARCHAR(300) NOT NULL,
                              observed_at TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    -- ADD CONSTRAINTS
                              CONSTRAINT pk_observations PRIMARY KEY (id), -- primary key assignment
                              CONSTRAINT fk_observations_creatures FOREIGN KEY (creature_id) REFERENCES creatures(id), -- Link id for creature reference
                              CONSTRAINT chk_observer_name CHECK ( observer_name <> '' ), -- ensure name isn't empty
                              CONSTRAINT chk_observer_note CHECK ( note_text <> '' ) -- ensure note is not blank
);

